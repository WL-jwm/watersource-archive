// ===== 水源地信息接口 =====
export interface WaterSourceInfo {
  /** 水源地名称 */
  name: string;
  /** 水源类型：地表水/地下水 */
  type: '地表水' | '地下水';
  /** 细分类型：湖库型/孔隙水/岩溶水/南水北调等 */
  subType?: string;
  /** 所在县区 */
  county: string;
  /** 使用状态 */
  status:
    '在用' | '备用' | '取消' | '规划' | '热备用' | '在建' | '应急' | '停用' | '已取消' | '已撤销';
  /** 备注 */
  remark?: string;
  /** 供水人口（万人） */
  population?: number;
  /** 所在河流/流域 */
  river?: string;
}

// ===== 市级水源地信息 =====
export interface CityWaterSources {
  /** 市名称 */
  cityName: string;
  /** 市级行政区划代码 */
  cityCode: string;
  /** 地市级（市级）集中式饮用水水源 */
  municipal: WaterSourceInfo[];
  /** 县级集中式饮用水水源 */
  county: WaterSourceInfo[];
  /** 乡镇级集中式饮用水水源 */
  township?: WaterSourceInfo[];
}

