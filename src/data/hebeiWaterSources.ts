// ===== 辅助函数 =====

/** 获取某市所有水源地（市级+县级+乡镇级） */
export function getCityWaterSources(cityName: string): WaterSourceInfo[] {
  const city = hebeiWaterSources.find((c) => c.cityName === cityName);
  if (!city) return [];
  return [...city.municipal, ...city.county, ...(city.township || [])];
}

/** 获取某市市级水源地 */
export function getCityMunicipalSources(cityName: string): WaterSourceInfo[] {
  const city = hebeiWaterSources.find((c) => c.cityName === cityName);
  return city?.municipal ?? [];
}

/** 获取某市县级水源地 */
export function getCityCountySources(cityName: string): WaterSourceInfo[] {
  const city = hebeiWaterSources.find((c) => c.cityName === cityName);
  return city?.county ?? [];
}

/** 获取某市乡镇级水源地 */
export function getCityTownshipSources(cityName: string): WaterSourceInfo[] {
  const city = hebeiWaterSources.find((c) => c.cityName === cityName);
  return city?.township ?? [];
}

/** 按水源类型筛选 */
export function filterByType(
  sources: WaterSourceInfo[],
  type: '地表水' | '地下水',
): WaterSourceInfo[] {
  return sources.filter((s) => s.type === type);
}

/** 按县区筛选 */
export function filterByCounty(sources: WaterSourceInfo[], county: string): WaterSourceInfo[] {
  return sources.filter((s) => s.county.includes(county));
}

/** 全省水源地统计 */
export function getHebeiWaterSourceStats() {
  let totalMunicipal = 0;
  let totalCounty = 0;
  let totalSurface = 0;
  let totalGround = 0;

  let totalTownship = 0;

  hebeiWaterSources.forEach((city) => {
    totalMunicipal += city.municipal.length;
    totalCounty += city.county.length;
    totalTownship += (city.township || []).length;
    city.municipal.forEach((s) => (s.type === '地表水' ? totalSurface++ : totalGround++));
    city.county.forEach((s) => (s.type === '地表水' ? totalSurface++ : totalGround++));
    (city.township || []).forEach((s) => (s.type === '地表水' ? totalSurface++ : totalGround++));
  });

  return {
    totalCities: hebeiWaterSources.length,
    totalMunicipal,
    totalCounty,
    totalTownship,
    total: totalMunicipal + totalCounty + totalTownship,
    totalSurface,
    totalGround,
    surfaceRatio:
      totalSurface + totalGround > 0
        ? ((totalSurface / (totalSurface + totalGround)) * 100).toFixed(1) + '%'
        : '0%',
  };
}
