// 快速查找函数
export function getCityByCode(code: string) {
  return hebeiDivisions.cities.find((c) => c.code === code);
}

export function getDistrictByCode(cityCode: string, districtCode: string) {
  const city = getCityByCode(cityCode);
  return city?.districts.find((d) => d.code === districtCode);
}

export function getAllDistricts() {
  return hebeiDivisions.cities.flatMap((c) => c.districts);
}

export function getDistrictTypeLabel(type: string): string {
  const map: Record<string, string> = {
    市辖区: '市辖区',
    县级市: '县级市',
    县: '县',
    自治县: '自治县',
  };
  return map[type] || type;
}
